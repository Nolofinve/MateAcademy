// --- Rectangle 5 x 8
printRectInfo(5, 8);

function printRectInfo(a, b) {
    const perimeter = 2 * a + 2 * b;
    const sqare = a * b;

    console.log(`Rectangle ${a} x ${b}`);
    console.log(`Perimeter is ${perimeter}`);
    console.log(`Square is ${sqare}`);
    console.log(`---------------`);
}

// --- Rectangle 3 x 10
printRectInfo(3, 10);